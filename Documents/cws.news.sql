DROP PROCEDURE IF EXISTS `cws`.`getNewsList`$$
/*!50003 SET @TEMP_SQL_MODE=@@SQL_MODE, SQL_MODE='STRICT_TRANS_TABLES,NO_AUTO_CREATE_USER' */ $$
CREATE PROCEDURE `cws`.`getNewsList`(
    IN lang VARCHAR(2)
)
BEGIN
    SELECT
        NEWS_ID,
        POSTING_DATE,
        UNPOSTING_DATE,
        NEWS_TITLE,
        NEWS_SHORT_DESC,
        NEWS_LANG,
        NEWS_DESCRIPTION
    FROM `cws`.`news`
    WHERE JOB_LANG = lang
    ORDER BY POSTING_DATE DESC;
END $$
