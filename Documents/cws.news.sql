DROP TABLE IF EXISTS `cws`.`news`;
CREATE TABLE `cws`.`news` (
    `NEWS_ID` VARCHAR(128) CHARACTER SET UTF8 NOT NULL,
    `POSTING_DATE` DATE NOT NULL,
    `UNPOSTING_DATE` DATE,
    `NEWS_TITLE` TEXT CHARACTER SET UTF8 NOT NULL,
    `NEWS_SHORT_DESC` TEXT CHARACTER SET UTF8 NOT NULL,
    `NEWS_LANG` VARCHAR(2) CHARACTER SET UTF8 NOT NULL DEFAULT 'en',
    `NEWS_DESCRIPTION` TEXT CHARACTER SET UTF8 NOT NULL,
    PRIMARY KEY (`NEWS_ID`),
    FULLTEXT KEY `FK_search_jobs` (`NEWS_ID`, `NEWS_TITLE`, `NEWS_SHORT_DESC`, `NEWS_DESCRIPTION`)
) ENGINE=MyISAM DEFAULT CHARSET=UTF8 ROW_FORMAT=COMPACT COLLATE UTF8_GENERAL_CI;
