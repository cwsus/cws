--
-- Definition of table `cws`.`email_messages`
--
DROP TABLE IF EXISTS `cws`.`email_messages`;
CREATE TABLE `cws`.`email_messages` (
    `EMAIL_MESSAGE_ID` VARCHAR(128) CHARACTER SET UTF8 NOT NULL UNIQUE,
    `EMAIL_MESSAGE_DATE` BIGINT NOT NULL,
    `EMAIL_MESSAGE_FROM` VARCHAR(100) CHARACTER SET UTF8 NOT NULL,
    `EMAIL_MESSAGE_TO` VARCHAR(255) CHARACTER SET UTF8 NOT NULL,
    `EMAIL_MESSAGE_CC` VARCHAR(255)CHARACTER SET UTF8,
    `EMAIL_MESSAGE_BCC` VARCHAR(255)CHARACTER SET UTF8,
    `EMAIL_MESSAGE_SOURCES` TEXT CHARACTER SET UTF8 NOT NULL,
    `EMAIL_MESSAGE_SUBJECT` VARCHAR(255) CHARACTER SET UTF8,
    `EMAIL_MESSAGE_BODY` TEXT CHARACTER SET UTF8 NOT NULL,
    `EMAIL_MESSAGE_ATTACHMENTS` BLOB,
    PRIMARY KEY  (`EMAIL_MESSAGE_ID`),
    FULLTEXT KEY `EMAIL_SEARCH` (`EMAIL_MESSAGE_FROM`, `EMAIL_MESSAGE_BODY`, `EMAIL_MESSAGE_SUBJECT`, `EMAIL_MESSAGE_SOURCES`)
) ENGINE=MyISAM DEFAULT CHARSET=UTF8 ROW_FORMAT=COMPACT COLLATE UTF8_GENERAL_CI;
COMMIT;

ALTER TABLE `cws`.`email_messages` CONVERT TO CHARACTER SET UTF8 COLLATE UTF8_GENERAL_CI;
COMMIT;

