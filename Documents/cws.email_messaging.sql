    IN attributeName VARCHAR(100)
)
BEGIN
    SELECT
        EMAIL_MESSAGE_ID,
        EMAIL_MESSAGE_DATE,
        EMAIL_MESSAGE_FROM,
        EMAIL_MESSAGE_TO,
        EMAIL_MESSAGE_CC,
        EMAIL_MESSAGE_BCC,
        EMAIL_MESSAGE_SOURCES,
        EMAIL_MESSAGE_SUBJECT,
        EMAIL_MESSAGE_BODY,
        EMAIL_MESSAGE_ATTACHMENTS,
