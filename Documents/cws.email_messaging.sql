    AGAINST (+attributeName WITH QUERY EXPANSION)
    FROM `cws`.`email_messages`
    WHERE MATCH (`EMAIL_MESSAGE_FROM`, `EMAIL_MESSAGE_BODY`, `EMAIL_MESSAGE_SUBJECT`, `EMAIL_MESSAGE_SOURCES`)
    AGAINST (+attributeName IN BOOLEAN MODE);
END $$
